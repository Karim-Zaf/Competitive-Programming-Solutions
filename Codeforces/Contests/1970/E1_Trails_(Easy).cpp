int binpow(int a, int k, int res = 1, int Mod = mod) {
    while (k) {
        if (k % 2)res = (res * a) % Mod;
        a = (a * a) % Mod;
        k /= 2;
    }
    return res;
}

int modinv(int a, int Mod = mod) { return binpow(a, Mod - 2); }

int mult(int a, int b, int Mod = mod) {
    b %= Mod;
    return a % Mod * b % Mod;
}

int add(int a, int b, int Mod = mod) { return ((a % Mod + b % Mod) % Mod + Mod) % Mod; }

int divide(int a, int b, int Mod = mod) {
    a %= Mod;
    b = modinv(b) % Mod;
    return (a * b) % Mod;
}


int n, m;
int dp[105][1005];
vector<int> s(105), l(105);

int work(int pos, int day) {

    if (day > n) return 1;

    int &ret = dp[pos][day];
    if (ret != -1) return ret;

    ret = 0;

    for (int to = 1; to <= m; to++) {
        int one = mult(s[pos], l[to]);
        int two = mult(l[pos], s[to]);
        int three = mult(s[pos], s[to]);

        int sum  =  one;
        sum = add(sum, two);
        sum = add(sum, three);
        sum = mult(sum, work(to, day + 1));

        ret = add (ret,sum) ;
    }

    return ret;
}

