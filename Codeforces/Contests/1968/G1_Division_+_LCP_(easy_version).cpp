vector<int> z_function(string s) {
    int n = s.size();
    vector<int> z(n);
    int l = 0, r = 0;
    for(int i = 1; i < n; i++) {
        if(i < r) {
            z[i] = min(r - i, z[i - l]);
        }
        while(i + z[i] < n && s[z[i]] == s[i + z[i]]) {
            z[i]++;
        }
        if(i + z[i] > r) {
            l = i;
            r = i + z[i];
        }
    }
    return z;
}

void Solve() {
    int n, lef, righ, ans = 0;
    string s;
    cin >> n >> lef >> righ >> s;

    vector<int> z= z_function(s) ;


    int l = 1, r = n;

    while (l <= r) {
        int md = l + (r - l) / 2, cnt = 0, i = md;

        if (md==0) md++;
        while (i < n) {
            if (z[i] >= md) {
                i += md;
                cnt++;
            } else i++;
        }

        if (cnt + 1 >= lef) {
            l = md + 1;
            ans = md;
        } else r = md - 1;
    }
    cout << ans << endl;


}

