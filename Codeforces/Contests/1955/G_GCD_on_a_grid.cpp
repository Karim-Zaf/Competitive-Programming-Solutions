const int N = 3e5 + 30, mod = 1e9 + 7, INF = 1e18 + 10;

int t[105][105];

void Solve() {
    int n, m;
    cin >> n >> m;
    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= m; j++)
            cin >> t[i][j];


    vector<int> divisors;
    int x = t[1][1];
    for (int d = 1; d * d <= x; d++) {
        if (x % d) continue;
        divisors.pb(d);
        divisors.pb(x / d);
    }

    sort(rall(divisors));
    for (auto d: divisors) {
        int dp[n + 1][m + 1];
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= m; j++) {
                dp[i][j] = (i == 1 && j == 1);
                dp[i][j] |= (t[i][j] % d == 0 && dp[i - 1][j] && i - 1);
                dp[i][j] |= (t[i][j] % d == 0 && dp[i][j - 1] && j - 1);
            }
        }
        if (dp[n][m]) {
            cout << d << endl;
            return;
        }
    }
}

